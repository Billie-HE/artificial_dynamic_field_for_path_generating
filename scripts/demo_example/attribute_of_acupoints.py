def guess_group(name):
    if "肩" in name:
        return "肩部"
    elif any(x in name for x in ["背", "脊", "柱", "灵台", "中膂", "身柱"]):
        return "背部"
    elif any(x in name for x in ["肾", "腰", "命门", "关元"]):
        return "腰部"
    elif any(x in name for x in ["膀胱", "肠", "脾", "胃", "胆", "肝", "小肠"]):
        return "腹部"
    else:
        return "全身"

